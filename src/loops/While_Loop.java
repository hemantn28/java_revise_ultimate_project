package loops;

public class While_Loop {

    public static void main(String[] args) {

        int i=0;
        while(i<5){
            System.out.println("hello "+i);
            i++;
        }
        int j=0;
        while (j<10){
            System.out.println("india "+j);
            j++;
        }
        int k=1;
        while (k<=8){
            System.out.println("alt "+k);
            k++;
        }

        int y=5;
        while (y<=10){
            System.out.println("enter "+y);
            y++;
        }

        int a=18;
        while (a<=24){
            System.out.println("num "+a);
            a++;
        }
        int ii=21;
        while (ii<25){
            System.out.println("ctrl "+ii);
            ii++;
        }
    }
}
