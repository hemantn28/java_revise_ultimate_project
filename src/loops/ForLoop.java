package loops;

public class ForLoop {

    public static void main(String[] args) {

        for (int i=0; i<5; i++){
            System.out.println("hello "+i);
        }

        for (int i=1; i<6; i++){
            System.out.println("india "+i);
        }

        for (int i=2; i<=7; i++){
            System.out.println("shift "+i);
        }
        for (int i=3; i<=9; i++){
            System.out.println("krishna "+i);
        }

        for (int i=65; i<70; i++){
            System.out.println("enter "+i);
        }

        for (int t=100; t<105; t++){
            System.out.println("hundered "+t);
        }
        for (int k=5; k>0; k--){
            System.out.println("one "+k);
        }
        for (int y=10; y>=0;y--){
            System.out.println("sun "+y);
        }
        for (int i=100; i>=1; i--){
            System.out.println("shift "+i);
        }

        for (int i=65; i>59; i--){
            System.out.println("insert "+i);
        }
    }
}
