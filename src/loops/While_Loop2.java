package loops;

public class While_Loop2 {

    public static void main(String[] args) {

        int i=0;
        while (i<5){
            System.out.println("hello "+i);
            i++;
        }

        int j=1;
        while (j<10){
            System.out.println("india "+j);
            j++;
        }

        int k=21;
        while (k<26){
            System.out.println("namaskar "+k);
            k++;
        }

        int l=45;
        while (l<50){
            System.out.println("type "+l);
            l++;
        }

        int m=56;
        while (m<=60){
            System.out.println("sixty "+m);
            m++;
        }
        int h=100;
        while (h>95){
            System.out.println("hundered "+h);
            h--;
        }
    }
}
