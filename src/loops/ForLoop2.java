package loops;

public class ForLoop2 {

    public static void main(String[] args) {

        for (int i=0; i<5;i++){
            System.out.println("Vikas "+i);
        }

        for (int j=1; j<=5;j++){
            System.out.println("india "+j);
        }

        for (int k=21; k<=25; k++){
            System.out.println("laptop "+k);
        }
        for (int l=66; l<=70; l++){
            System.out.println("Asus "+l);
        }

        for (int m=5; m>0; m--){
            System.out.println("hello "+m);
        }

        for (int n=10; n>=1; n--){
            System.out.println("search "+n);
        }

        for (int o=30; o>25;o--){
            System.out.println("num "+o);
        }
    }
}
